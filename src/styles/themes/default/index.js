import createApi from 'cssapi';
import globalStyles from './globalStyles';

// -----------------------------------------------------------------------------
// Define Breakpoints
// -----------------------------------------------------------------------------

const breakpoints = [
  ['smallUp', 400], // 400–900
  ['mediumUp', 900], // 900–1100
  ['largeUp', 1100], // 1100–
];

// -----------------------------------------------------------------------------
// Configure CSS API
// -----------------------------------------------------------------------------

const config = {
  breakpoints,
  data: {
    color: {
      black: '#070707',
      white: '#F5F5F5',
      bg: 'c:white',
      text: 'c:black',
    },
    scale: {
      smallprint: '12',
      body: '16',
      secondaryTitle: '20',
      primaryTitle: '24',
    },
    font: {
      all: 'Cousine, monospace',
    },
  },
};

const api = createApi(config);

// -----------------------------------------------------------------------------
// Global Styles
// -----------------------------------------------------------------------------

globalStyles(api);

// -----------------------------------------------------------------------------
// Export Theme
// -----------------------------------------------------------------------------

export default {
  api,
};
